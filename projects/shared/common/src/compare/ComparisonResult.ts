/**
 * Represents the result of a comparison operation.
 *
 * @property {-1} - Indicates that the first value is less than the second value.
 * @property {0} - Indicates that the first value is equal to the second value.
 * @property {1} - Indicates that the first value is greater than the second value.
 */
export type ComparisonResult = -1 | 0 | 1;
